function formatter(s, q) {
  s.split("");
  let out = [];
  q.forEach(arr => {
    if (arr[0] === "I") {
      for (let i = arr[1] - 1; i <= arr[2]; i++) {
        s[i] = s[i] == "1" ? "0" : "1";
      }
    } else if (arr[0] === "Q") {
      out.push(s[arr[1] - 1]);
    }
  });

  return out;
}

